list = [1, 3, 5, 7, 9, 11, 13]


num_s = int(input("Ingrese un número: "))

# Utilizar el operador "in" para verificar si el número está en la lista
if num_s in list:
    print(f"El número {num_s} está presente en la lista.")
else:
    print(f"El número {num_s} no está presente en la lista.")







